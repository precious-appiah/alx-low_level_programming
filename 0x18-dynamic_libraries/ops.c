#include <stdio.h>

/**
 * add- func name
 * @a: param1
 * @b:param2
 * Return: int
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub- func name
 * @a: param1
 * @b:param2
 * Return: int
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul- func name
 * @a: param1
 * @b:param2
 * Return: int
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div- func name
 * @a: param1
 * @b:param2
 * Return: double
 *
 */
double div(int a, int b)
{
	return ((double)a / b);
}

/**
 * mod- func name
 * @a: param1
 * @b:param2
 * Return: int
 */
int mod(int a, int b)
{
	return (a % b);
}
